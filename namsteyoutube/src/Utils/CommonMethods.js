export function likeCount(like) {

  if(like==undefined) return;
  let length = like.length;
  let likesToReturn;
  switch (length) {
    case (1, 2, 3, 4):
      likesToReturn = like;
      break;
    case (5, 6):
      likesToReturn = like / 1000 + "";
      likesToReturn = likesToReturn.substring(0, 4) + "K";
      break;
    default:
      likesToReturn = like / 1000000 + "";
      likesToReturn = likesToReturn.substring(0, 4) + "M";
  }
  return likesToReturn;
}

export function calculateTimeDiff(publishTime) {
  let seconds = Math.floor(
    (Date.now() - new Date(publishTime).getTime()) / 1000
  );
  let minutes = Math.floor(seconds / 60);
  let hours = Math.floor(minutes / 60);
  let days = Math.floor(hours / 24);
  let months=Math.floor(days/30);
  let years =Math.floor(months/12)
 if (years > 0) return years + " years";
  else if (months > 0) return months + " months";
 else if (days > 0) return days + " days";
  else if (hours > 0) return hours + " hours";
  else if (minutes > 0) return minutes + " minutes";
  else if (seconds > 0) return seconds + " seconds";
}
